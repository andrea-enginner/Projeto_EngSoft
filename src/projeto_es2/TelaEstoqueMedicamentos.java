    public void setTableMedi(String nome, String quantidade, int linha){
        
        TableMedi.setValueAt(nome, linha, 0);
        TableMedi.setValueAt(quantidade, linha, 1);
    }
    
    public void SetTable(){
        M.readMedicamento();
        
        int i;
        int size = M.getSize();
        
        for(i = 0;i < size; i++){
            
            M.setMedicamento(i);
        
            DefaultTableModel tableMedi= (DefaultTableModel) TableMedi.getModel();
        
            Object[] newrow = {M.getNomemedicamento(),M.getQuantidade()};
        
            tableMedi.addRow(newrow);
        }
        
        M.clearList();
    }

