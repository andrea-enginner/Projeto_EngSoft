    private void ButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAdicionarActionPerformed
        // TODO add your handling code here:
        M.setNomemedicamento(TextMed.getText());
        M.setQuantidade(TextQuant.getText());
        
        if(M.getNomemedicamento().isEmpty() || M.getQuantidade().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por Favor, Preencha todos os campos!");
            return;
        }
        
        M.addMedicamento();
        
        DefaultTableModel tableMedi = (DefaultTableModel) TableMedi.getModel();
        
        Object[] newrow = {M.getNomemedicamento(),M.getQuantidade()};
        
        tableMedi.addRow(newrow);
        
        TextMed.setText("");
        TextQuant.setText("");
        JOptionPane.showMessageDialog(this, "Medicamento adicionado com sucesso!");
        
    }//GEN-LAST:event_ButtonAdicionarActionPerformed

    private void TableMediMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMediMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 1){
            int linha = TableMedi.getSelectedRow();
            
            TextMed.setText((String)TableMedi.getValueAt(linha, 0));
            TextQuant.setText((String)TableMedi.getValueAt(linha, 1));
        }
    }//GEN-LAST:event_TableMediMouseClicked

    private void ButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = TableMedi.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Por Favor, Selecione uma Linha da Tabela!");
        }else{
            
            M.setNomemedicamento(TextMed.getText());
            M.setQuantidade(TextQuant.getText());
            
            if(M.getNomemedicamento().isEmpty() || M.getQuantidade().isEmpty()){
                JOptionPane.showMessageDialog(this, "Por Favor, Preencha todos os campos!");
                return;
        }
            
            M.updateNomemedicamento(M, M.getNomemedicamento());
            M.updateQtdmedicamento(M);
            
            setTableMedi(M.getNomemedicamento(),M.getQuantidade(),linhaSelecionada);
            
            TextMed.setText("");
            TextQuant.setText("");
            JOptionPane.showMessageDialog(this, "Medicamento editado com sucesso!");
        }
    }//GEN-LAST:event_ButtonEditarActionPerformed

    private void ButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = TableMedi.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Por Favor, Selecione uma Linha da Tabela!");
        }
        
        DefaultTableModel tableMedi = (DefaultTableModel) TableMedi.getModel();
        
        //CPF para busca no Banco de Dados
        M.setNomemedicamento((String)TableMedi.getValueAt(linhaSelecionada, 0));
        
        M.deleteMedicamento();
        
        //Remover as informações do funcionário selecionado da tabela
        
        tableMedi.removeRow(linhaSelecionada);
        
        TextMed.setText("");
        TextQuant.setText("");
        
        JOptionPane.showMessageDialog(this, "Medicamento excluido com sucesso!");
    }//GEN-LAST:event_ButtonExcluirActionPerformed

